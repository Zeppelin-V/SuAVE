function generateImgJson(options){
  var result = [];
  for(var i = 0; i < options.length; i++){
    data = options[i];
    if(data.length == 0) data = "Null";
    result.push({'text': data,
                  'value': data,
                  'selected': false,
                  'imageSrc': "/../../img/blue.jpg"}
    );
  }
  return result;
}
